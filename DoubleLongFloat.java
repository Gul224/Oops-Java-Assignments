public class DoubleLongFloat {
    public static void main(String[] args){
        int a = 9;

        // long should L along with the digit at the end
        long b = 9999999999999L;
        //float should have f at the end
        float c = 2.5f;

        //double will have many digits but without any weird letter at the end
        double d = 5.4;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);

    }
}
